#!/bin/bash
# Awk Example for Formated Output
awk -F":" '
BEGIN {
print "======================================================================================================="
printf  "%1s %-15s %-3s %-3s %-25s %-15s %-33s %1s\n" , "|", "User", "UID", "GID", "Home", "Shell", "About", "|"
print "======================================================================================================="
}
{ 
if($7 != "/sbin/nologin") 
	{ 
	printf "%1s %-15s %3d %3d %-25s %-15s %-33s %1s\n", "|",$1,$3,$4,$6,$7,$5,"|"
	} 


}
END {
	print "======================================================================================================="
}' /etc/passwd
